package com.alps.shisu.modelclass;

public class PackageDetails {

    public String getKitId() {
        return kitId;
    }

    public void setKitId(String kitId) {
        this.kitId = kitId;
    }

    public String getKitCode() {
        return kitCode;
    }

    public void setKitCode(String kitCode) {
        this.kitCode = kitCode;
    }

    public String getKitPrice() {
        return kitPrice;
    }

    public void setKitPrice(String kitPrice) {
        this.kitPrice = kitPrice;
    }

    public String getKitDesc() {
        return kitDesc;
    }

    public void setKitDesc(String kitDesc) {
        this.kitDesc = kitDesc;
    }

    private String kitId;
    private String kitCode;
    private String kitPrice;
    private String kitDesc;

}
